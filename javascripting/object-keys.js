//Claves de objetos - Object keys
const car = {
    make: 'Honda',
    model: 'Accord',
    year: 2020
}
//Usamos el comando Object.keys(), para llamar el nombre de los objetos.
//We use the command Object.keys(), to call the name of the objects
const keys = Object.keys(car) 

console.log(keys) // Imprimimos el resultado - We print the result