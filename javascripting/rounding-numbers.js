//Redondeando numeros - Rounding numbers

let roundUp = 1.5; //Declarando la variable - Declaring the variable.

let rounded = Math.round(roundUp); // Usamos el operador Math.round() para redondear el resultado, y lo guardamos en una variable - Use the operator Math.round() for rounding the result, and store it in a variable.

console.log(rounded); // Impresion del resultado - Print the result